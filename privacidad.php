		<h1>Política de privacidad</h1>
		Esta política de privacidad establece el uso de la aplicación móvil "Appdate Legislativo", creada por Proweb Solutions.
		<br/><br/>
		<h2>¿Qué información obtiene el aplicativo y como es utilizada?</h2>
		<i>Información proveída por el usuario</i>
		<br/><br/>
		El aplicativo no requiere ningún tipo de registro, pero si requiere que el usuario mantenga una cuenta en Appdate Legislativo para acceder a noticias personalizadas y funciones como favoritos o push notifications.
		<br/><br/>
		<i>Información obtenida automáticamente</i>
		<br/><br/>
		Adicionalmente, el aplicativo puede recopilar de forma automática otro tipo de información, como ser: tipo de dispositivo móvil, país del usuario, dirección IP, 
		el sistema operativo del dispositivo móvil e información de cómo se usa el aplicativo.
		<br/>
		<h2>¿El aplicativo obtiene la información precisa y en tiempo real del dispositivo?</h2>
		El aplicativo no recopila información precisa y en tiempo real de la ubicación del dispositivo en ningún momento. 
		<br/>
		<h2>¿Pueden terceros obtener acceso a la información obtenida por el aplicativo?</h2>
		Si.  Podemos compartir la información proporcionada por el usuario o la obtenida automáticamente en los siguientes casos:
		<ol>
			<li>Cuando sea requerido por la ley.</li>
			<li>Cuando creamos que es necesario para proteger nuestros derechos, proteger su seguridad o la seguridad de otros, investigar fraude o en respuesta a una solicitud del gobierno.</li>
			<li>Con proveedores que trabajan en nuestro nombre y que han aceptado regirse por las reglas establecidas en esta política de privacidad.</li>
		</ol>
		<h2>Política de retención y manejo de información</h2>
		Conservaremos los datos proveídos por el usuario y los recolectados automáticamente de forma indefinida, ya que se necesita para el uso correcto del aplicativo.
		<br/>
		<h2>Seguridad</h2>
		Nos preocupamos por  salvaguardar la confidencialidad de su información, por lo que contamos con las medidas necesarias para lograr este fin. 
		Por ejemplo, limitamos el acceso a esta información a los empleados autorizados que necesitan conocer dicha información para operar, desarrollar o mejorar nuestra aplicación. 
		Tenga en cuenta que nos esforzamos por proveer un nivel de seguridad razonable para manejar y procesar la información, pero ningún sistema de seguridad puede prevenir todas las 
		brechas de seguridad potenciales.
